// Definição dos números: Dois números, numero1 e numero2, são escolhidos e atribuídos o valor 5 cada um.
int numero1 = 5;
int numero2 = 5;

// Soma dos números: Os valores de numero1 e numero2 são adicionados e o resultado é guardado na variável resultado.
int resultado = numero1 + numero2;

// Criação da mensagem: A parte fixa da mensagem "Resultado da soma é " é combinada com o valor de resultado usando o operador de adição (+).
string mensagem = "Resultado da soma é " + resultado;

// Impressão do resultado: A mensagem completa é exibida no console para mostrar o valor da soma.
Console.WriteLine(mensagem); // O valor da soma será exibido no console.


//Resumidamente, o código recebe dois números, soma esses números e mostra o resultado no console. 
//Utiliza-se a concatenação para juntar a mensagem fixa com o valor da soma e criar a mensagem completa.