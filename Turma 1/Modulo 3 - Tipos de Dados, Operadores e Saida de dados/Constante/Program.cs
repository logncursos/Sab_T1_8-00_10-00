// Definindo três constantes: 'NumeroMaximo', 'Pi' e 'Saudacao'.
// 'NumeroMaximo' é do tipo int (inteiro) e recebe o valor 100.
const int NumeroMaximo = 100;

// 'Pi' é do tipo double (número decimal) e recebe o valor 3.14.
const double Pi = 3.14;

// 'Saudacao' é do tipo string (cadeia de caracteres) e recebe o valor "Olá, mundo!".
const string Saudacao = "Olá, mundo!";

// Exibindo os valores das constantes no Console.
// A expressão entre as aspas e o sinal de + é usada para concatenar (juntar) os valores das constantes com as mensagens de texto.
// Por exemplo, a mensagem "Número máximo: " + NumeroMaximo resultará em "Número máximo: 100" na saída.
Console.WriteLine("Número máximo: " + NumeroMaximo);
Console.WriteLine("Valor de Pi: " + Pi);
Console.WriteLine("Mensagem de saudação: " + Saudacao);
